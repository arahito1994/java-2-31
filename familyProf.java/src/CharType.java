public class CharType {
  public static void main(String[] args) {
    char alphabet = 'E';
    char number = '9';
    char symbol = '%';
    char JapaneseChar = 'あ';

  System.out.printf("英字 = %c%n", alphabet);
  System.out.printf("数字 = %c%n", number);
  System.out.printf("記号 = %c%n", symbol);
  System.out.printf("日本語 = %c%n", JapaneseChar);
  }
}
